package Task2;

public class Methods {

    int a, b;

    public double sum(int a, int b) {

        double sum = a + b;
        return sum;

    }

    public double sub(int a, int b) {

        double sub = a - b;
        return sub;

    }

    public double div(int a, int b) {

        double div = a / b;
        return div;

    }

    public double mul(int a, int b) {

        double mul = a * b;
        return mul;

    }

}
